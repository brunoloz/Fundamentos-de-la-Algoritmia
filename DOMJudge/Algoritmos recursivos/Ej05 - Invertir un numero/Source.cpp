// función que resuelve el problema
int inverso(int n, int suma_acu) {

    if (n == 0) return suma_acu;

    suma_acu = suma_acu * 10 + n % 10;
        
     return inverso(n / 10, suma_acu);


}

// Resuelve un caso de prueba, leyendo de la entrada la
